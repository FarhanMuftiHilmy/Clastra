export type Locale = keyof typeof translations;

type TranslationsForNamespace<N extends keyof typeof translations['en']> = keyof typeof translations['en'][N];

export type TranslationPath =
  | `admin.${TranslationsForNamespace<'admin'>}`
  | `auth.${TranslationsForNamespace<'auth'>}`
  | `teacher.${TranslationsForNamespace<'teacher'>}`
  | `common.${TranslationsForNamespace<'common'>}`;

type Interpolation = Record<string, string | number>;

const storedLocale = typeof window !== 'undefined' ? window.localStorage.getItem('app.locale') : null;
const browserLocale = typeof navigator !== 'undefined' ? navigator.language.split('-')[0] : null;

export const supportedLocales: Locale[] = Object.keys(translations) as Locale[];

const initialLocale: Locale = (storedLocale && supportedLocales.includes(storedLocale as Locale))
  ? (storedLocale as Locale)
  : (browserLocale && supportedLocales.includes(browserLocale as Locale))
    ? (browserLocale as Locale)
    : 'en';

let locale: Locale = initialLocale;

function interpolate(template: string, params?: Interpolation): string {
  if (!params) return template;
  return Object.keys(params).reduce((text, key) => {
    return text.replace(new RegExp(`{{${key}}}`, 'g'), String(params[key]));
  }, template);
}

export function setLocale(newLocale: Locale): void {
  locale = translations[newLocale] ? newLocale : 'en';
  if (typeof window !== 'undefined') {
    window.localStorage.setItem('app.locale', locale);
  }
}

export function getLocale(): Locale {
  return locale;
}

export function t(path: TranslationPath, params?: Interpolation): string {
  const [namespace, key] = path.split('.') as [keyof typeof translations['en'], string];
  const value = translations[locale][namespace][key as keyof typeof translations['en'][typeof namespace]];
  return interpolate(value as string, params);
}
